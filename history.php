                            	<h1>История Английского языка</h1>
                            	<br>
                                <p>
                            	История английского языка — процесс, в результате которого диалект одного из германских племен, на котором в V веке говорили жители Британии, за полтора тысячелетия превратился в язык, на котором общаются свыше 2 млрд. человек по всему миру.</p><br>
                                <br>
                                <p>Английский язык является западногерманским языком, который возник на основе англо-фризских наречий, привнесённых в Британию в V  —  VII веках н. э. германскими завоевателями и поселенцами нынешней Северо-Западной Германии, Западной Дании и Нидерландов.</p><br>
                                <br>
                                <p>Старый английский язык англо-саксонской эпохи развился в среднеанглийский язык, на котором говорили со времён нормандских завоеваний и до конца XV века. Значительное влияние на формирование английского оказали контакты с северо германскими языками, на которых говорили скандинавы, завоевавшие и колонизировавшие Британию в с VIII по IX века; этот контакт привел к многочисленным лексическим заимствованиям и грамматическим упрощениям. Влияние на язык оказали завоевания норманн, которые говорили на французском, называемым старонормандским языком, который в Британии развился в англо-нормандский язык. Много норманнских и французских заимствований вошло в языковую лексику, относящуюся к церкви и судебной системе. Система орфографии, утвердившаяся в среднеанглийский период используется и сегодня.</p><br>
                                <br>
                                <p>Ранний современный английский язык — язык Шекспира, был распространен с, примерно, 1500 года. В нём нашли отражение многие заимствования эпохи Ренессанса с латинского и древнегреческого языков, а также заимствования из других европейских языков, включая французский, немецкий и голландский. Изменения в произношении в этот период включали в себя Великий сдвиг гласных (фонетические изменения в английском языке в XIV—XV веках), что сказалось на свойствах долгих гласных. Современный английский язык, на котором говорят и поныне, был в употреблении с конца XVII века. Со времён Британской колонизации английский язык получил распространение в Великобритании, Ирландии, США, Канаде, Австралии, Новой Зеландии, Индии, части Африки и в других странах. В настоящее время он также является средством межнационального общения — Лингва франка.</p><br>
                                <br>
                                <p>Старый английский язык включал в себя различные группы диалектов, отражающих происхождение англосаксонских королевств, созданных в разных районах Великобритании. В итоге Западно-Саксонский диалект языка стал доминирующим. Большое влияние на среднеанглийский язык оказал Древнеанглийский язык. Разновидностью английского языка является шотландский язык. На нём традиционно говорят в некоторых районах Шотландии и Северной Ирландии. Иногда он рассматривается, как самостоятельный язык.</p>
